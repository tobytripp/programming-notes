potion( IngredientA, IngredientB ) :- potion( IngredientA, IngredientB, _ ).

potion( IngredientA, IngredientB, Effect ) :-
    effect( IngredientA, Effect ),
    effect( IngredientB, Effect ),
    IngredientA \= IngredientB.

mixes( [], _, _, _ ) :- fail.
mixes( L, Ia, Ib, E ) :-
    permutation( L, [Ia|[Ib|_]] ),
    potion( Ia, Ib, E ).
